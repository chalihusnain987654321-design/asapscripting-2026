PROPERTY_IDS = list(PROPERTY_NAMES.keys())
REPORTING_ORDER = list(PROPERTY_NAMES.values())


def main():
    parser = argparse.ArgumentParser(description="GA4 Reporter")
    parser.add_argument("--start_date", required=True, help="Start date (YYYY-MM-DD)")
    parser.add_argument("--end_date", required=True, help="End date (YYYY-MM-DD)")
    parser.add_argument("--sheet_name", required=True, help="Google Sheet name to write results to")
    args = parser.parse_args()

    if not os.path.exists(SA_FILE):
        print(f"[ERROR] Service account file not found: {SA_FILE}", flush=True)
        sys.exit(1)

    # GA4 client
    ga_credentials = service_account.Credentials.from_service_account_file(SA_FILE)
    ga_client = BetaAnalyticsDataClient(credentials=ga_credentials)

    # Google Sheets client
    try:
        gs_client = gspread.service_account(filename=SA_FILE)
    except Exception as e:
        print(f"[ERROR] Failed to authenticate with Google Sheets: {e}", flush=True)
        sys.exit(1)

    try:
        sheet = gs_client.open(args.sheet_name).sheet1
    except gspread.SpreadsheetNotFound:
        print(
            f"[ERROR] Spreadsheet '{args.sheet_name}' not found. "
            f"Make sure the sheet exists and has been shared with this service account's email.",
            flush=True,
        )
        sys.exit(1)
    except Exception as e:
        print(f"[ERROR] Could not open Google Sheet '{args.sheet_name}': {e}", flush=True)
        sys.exit(1)

    print(f"[INFO] Date range: {args.start_date} to {args.end_date}", flush=True)
    print(f"[INFO] Google Sheet: {args.sheet_name}", flush=True)
    print(f"[INFO] Properties to process: {len(PROPERTY_IDS)}\n", flush=True)

    # Fetch data for each property — parallel with 20 threads
    date_range_label = f"{args.start_date} - {args.end_date}"
    total = len(PROPERTY_IDS)
    completed = 0
    rows = []

    def fetch_property(property_id):
        website_name = PROPERTY_NAMES[property_id]
        try:
            request = RunReportRequest(
                property=f"properties/{property_id}",
                dimensions=[Dimension(name="date")],
                metrics=[Metric(name="totalUsers")],
                date_ranges=[DateRange(start_date=args.start_date, end_date=args.end_date)]
            )
            response = ga_client.run_report(request)
            total_users = sum(int(row.metric_values[0].value) for row in response.rows)
            return (website_name, date_range_label, total_users, None)
        except Exception as e:
            return (website_name, date_range_label, 0, str(e)[:80])

    with ThreadPoolExecutor(max_workers=20) as executor:
        futures = {executor.submit(fetch_property, pid): pid for pid in PROPERTY_IDS}
        for future in as_completed(futures):
            completed += 1
            website_name, date_range_label_, total_users, err = future.result()
            rows.append([website_name, date_range_label_, total_users])
            if err:
                print(f"[WARN] [{completed}/{total}] Failed for {website_name}: {err}", flush=True)
            else:
                print(f"[INFO] [{completed}/{total}] {website_name}: {total_users} users", flush=True)

    # Sort to match REPORTING_ORDER and write to sheet
    df = pd.DataFrame(rows, columns=["website_name", "date_range", "total_users"])
    order_map = {name: i for i, name in enumerate(REPORTING_ORDER)}
    df["sort_order"] = df["website_name"].map(order_map).fillna(9999)
    df = df.sort_values("sort_order").drop(columns="sort_order")

    sheet.clear()
    sheet.update([df.columns.tolist()] + df.values.tolist())

    print(f"\n[DONE] Report written to Google Sheet: '{args.sheet_name}'")


if __name__ == "__main__":
    main()
